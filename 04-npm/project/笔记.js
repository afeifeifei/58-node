/*
    npm init / npm init -y
        项目初始化

    npm install 包名 参数  /  npm i 包名 参数
        安装
        参数：
            -S / --save   当前项目依赖的包 生产环境--项目上线之后依然会使用
            -D / --dev    当前项目依赖的包 开发环境--只在开发阶段会使用
            -g            全局安装

    npm uninstall 包名 参数 / npm un 包名 参数
        卸载

    npm update 包名
        更新   可以指定版本 包名@版本号

    npm list
        展示当前安装的包

    npm publish
        上传包到服务器
    npm unpublish
        从npm服务器删除当前包




    npm i cnpm -g
 */